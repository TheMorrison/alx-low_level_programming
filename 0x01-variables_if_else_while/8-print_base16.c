#include <stdio.h>
/**
 * main - Prints all the numbers of base 16 in lowercase
 * @void: Null value
 *
 * Return: 0
 */

int main(void)
{
	char alph;
	int num;

	for (num = '0'; num <= '9'; num++)
		putchar(alph);
	for (alph = 'a'; alph <= 'f'; alph++)
		putchar(alph);
	putchar('\n');
	return (0);
}
